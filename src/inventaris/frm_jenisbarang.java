    private void tabel() {
      String[] judul = {"ID","Nama Jenis","Kode Jenis","Keterangan"};
      model = new DefaultTableModel(judul,0);
      jTable1.setModel(model);
      String sql = "select * from jenis where nama_jenis like '%"+jTextField3.getText()+"%' or kode_jenis like '%"+jTextField3.getText()+"%'";
     
      try {
         con = koneksi.getConnection();
         rs = con.createStatement().executeQuery(sql);
         
         while(rs.next()) {
             String id = rs.getString("id_jenis");
             String namajenis = rs.getString("nama_jenis");
             String kodejenis = rs.getString("kode_jenis");
             String keterangan = rs.getString("keterangan");
             
             String[] data = {id,namajenis,kodejenis,keterangan};
             model.addRow(data);
         }
      }catch (Exception e) {
          
      }
      
    }

