    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String user = jTextField1.getText();
       String pass = jPasswordField1.getText();
      
       
       try {
           con = koneksi.getConnection();
           pst = con.prepareStatement("SELECT * FROM  petugas where username=? and password=?");
           pst.setString(1,user);
           pst.setString(2,pass);
           rs = pst.executeQuery();
           
            if (rs.next()) {
            String nama = rs.getString("nama_petugas");
            String usr = rs.getString("username");
            String pas = rs.getString("password");
            String level = rs.getString("id_level");
            
                if(usr.equals(user) && pas.equals(pass) && level.equals("1")){
                  frm_utama go = new frm_utama();
                  go.show();
                  this.hide();
                }
                else if(usr.equals(user) && pas.equals(pass) && level.equals("2")){
                  frm_utama go = new frm_utama(usr,nama,level);
                  go.show();
                  this.hide();
                }
                else if(usr.equals(user) && pas.equals(pass) && level.equals("3")){
                  frm_utama go = new frm_utama(1,usr,nama,level);
                  go.show();
                  this.hide();
                }
                  else{
                    jLabel4.setVisible(true);
                    jLabel4.setText("Perhatikan Huruf Besar Kecil");
                }
           
                } else {
                     jLabel4.setVisible(true);
            } 
            
            
           } catch (SQLException ex) {
                jLabel4.setVisible(true);
                System.out.println(ex);
        }                         
    }//GEN-LAST:event_jButton1ActionPerformed

